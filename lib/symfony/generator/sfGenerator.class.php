/**
 * sfGenerator is the abstract base class for all generators.
 *
 * @package    symfony
 * @subpackage generator
 * @author     Fabien Potencier <fabien.potencier@symfony-project.com>
 * @version    SVN: $Id$
 */
abstract class sfGenerator
{
  abstract public function generate($generator, $class, $param);

  protected function replacePhpMarks($text)
  {
    // replace [?php and ?]
    $text = str_replace('[?php', '<?php',      $text);
    $text = str_replace('[?=',   '<?php echo', $text);
    $text = str_replace('?]',    '?>',         $text);

    return $text;
  }

  public function generateClass($content, $clazz = __CLASS__)
  {
    $retval = "<?php\n".
              "// auto-generated by $clazz\n".
              "// date: %s\n%s\n?>";
    $retval = sprintf($retval, date('m/d/Y H:i:s'), $content);

    return $retval;
  }

  public function generateTemplate($content, $clazz = __CLASS__)
  {
    $retval = "<?php\n".
              "// auto-generated by $clazz\n".
              "// date: %s\n?>\n%s";
    $retval = sprintf($retval, date('m/d/Y H:i:s'), $content);

    return $retval;
  }
}

